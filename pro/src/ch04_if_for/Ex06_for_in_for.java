package ch04_if_for;

public class Ex06_for_in_for {

	public static void main(String[] args) {
					
					for(int i=0; i<5 ; i++) {
						for(int j=0; j<3; j++) {
							//System.out.printf(string format, object args); -가변인자를 가진메소드
							System.out.printf(" ["+i+","+j+"]  "); //=("[%d,%d]  ", i, j);
						}
						System.out.println();
						
					}
						
		
		
	}

}
